// C program to illustrate the arithmatic operators
#include <stdio.h>

int main(void)
{

    int a = 25, b = 5;

    // using operators and printing results
    printf("a + b = %d\n", a + b);
    printf("a - b = %d\n", a - b);
    printf("a * b = %d\n", a * b);
    printf("a / b = %d\n", a / b);
    printf("a % b = %d\n", a % b);
    printf("+a = %d\n", +a);
    printf("-a = %d\n", -a);
    printf("a++ = %d\n", a++);
    printf("a-- = %d\n", a--);


    // x-=3 decrease x by 3
    // x+=1 increase x by 1
    // x-=2 decrease x by 2

    printf("sum is %d\n", a+b);

    return 0;
}